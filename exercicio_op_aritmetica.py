import time
numero1 = int(input("digite o 1° número: "))
numero2 = int(input("digite o 2° número: "))

# Adição
print("\nAdição:", numero1 + numero2)

# Subtração
print("\nSubtração:", numero1 - numero2)

# Multiplicação
print("\nMultiplicação:", numero1 * numero2)

# Divisão
print("\nDivisão:", numero1 / numero2)

# Divisao arredondada
print("\nDivisão arredondada:", numero1 // numero2)

# Resto de divisão
print("\nResto de divisão:", numero1 % numero2)

# Potência 
print("\nPotência:", numero1 ** numero2)

time.sleep(10)